import * as mongoose from 'mongoose';

export const OrderSchema = new mongoose.Schema({
  orderDate: {
    type: Date,
    required: true,
  },
  productId: {
    type: String,
    required: true,
  },
  productName: {
    type: String,
    required: true,
  },
  shipped: {
    type: Number,
    required: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
});
